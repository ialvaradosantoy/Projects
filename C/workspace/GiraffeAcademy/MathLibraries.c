#include <stdio.h>
#include <stdlib.h>
#include <math.h>

int main ()
{
    printf("%d\n", pow(2,3));  // 2 raised to the 3rd power, power function
    printf("%d\n", sqrt(36)); // square root function
    printf("%d\n", ceil(36.6));  // cieling function to round up if possible
    printf("%d\n", floor(36.4));  // floor to round down
    
    return 0;
}